-- 1️⃣ STUDENTS TABLE
CREATE TABLE students (
                          id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          name VARCHAR2(100),
                          email VARCHAR2(100) UNIQUE,
                          password VARCHAR2(100),
                          college VARCHAR2(150),
                          phone VARCHAR2(20)
);

-- 2️⃣ MENTORS TABLE
CREATE TABLE mentors (
                         id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                         name VARCHAR2(100),
                         email VARCHAR2(100) UNIQUE,
                         password VARCHAR2(100),
                         expertise VARCHAR2(150),
                         experience NUMBER(3),
                         organization VARCHAR2(150)
);

-- 3️⃣ PROJECTS TABLE
CREATE TABLE projects (
                          id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          project_name VARCHAR2(200),
                          domain VARCHAR2(100),
                          budget NUMBER(15,2),
                          description CLOB,
                          tools VARCHAR2(200),
                          technologies VARCHAR2(200),
                          student_id NUMBER,
                          CONSTRAINT fk_project_student FOREIGN KEY (student_id)
                              REFERENCES students(id) ON DELETE CASCADE
);



-- 5️⃣ MENTOR REQUESTS TABLE
CREATE TABLE mentor_requests (
                                 id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                 student_id NUMBER,
                                 mentor_id NUMBER,
                                 project_id NUMBER,
                                 message VARCHAR2(500),
                                 status VARCHAR2(20) DEFAULT 'PENDING',
                                 CONSTRAINT fk_req_student FOREIGN KEY (student_id)
                                     REFERENCES students(id) ON DELETE CASCADE,
                                 CONSTRAINT fk_req_mentor FOREIGN KEY (mentor_id)
                                     REFERENCES mentors(id) ON DELETE CASCADE,
                                 CONSTRAINT fk_req_project FOREIGN KEY (project_id)
                                     REFERENCES projects(id) ON DELETE CASCADE
);

-- 6️⃣ FUNDERS TABLE
CREATE TABLE funders (
                         id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                         name VARCHAR2(100),
                         email VARCHAR2(100) UNIQUE,
                         password VARCHAR2(100),
                         organization VARCHAR2(150),
                         funding_amount NUMBER(15,2),
                         domain VARCHAR2(100),
                         description CLOB,
                         contact VARCHAR2(100)
);

